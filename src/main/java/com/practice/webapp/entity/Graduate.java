package com.practice.webapp.entity;

public class Graduate {
	private int gradId;
	private String gradName;
	private String gradPassword;
	private String gradPicture;
	private String gradPosition;
	private String gradCompany;
	private int gradWork;
	
	public int getGradId() {
		return gradId;
	}
	public void setGradId(int gradId) {
		this.gradId = gradId;
	}
	public String getGradName() {
		return gradName;
	}
	public void setGradName(String gradName) {
		this.gradName = gradName;
	}
	public String getGradPassword() {
		return gradPassword;
	}
	public void setGradPassword(String gradPassword) {
		this.gradPassword = gradPassword;
	}
	public String getGradPicture() {
		return gradPicture;
	}
	public void setGradPicture(String gradPicture) {
		this.gradPicture = gradPicture;
	}
	public String getGradPosition() {
		return gradPosition;
	}
	public void setGradPosition(String gradPosition) {
		this.gradPosition = gradPosition;
	}
	public String getGradCompany() {
		return gradCompany;
	}
	public void setGradCompany(String gradCompany) {
		this.gradCompany = gradCompany;
	}
	public int getGradWork() {
		return gradWork;
	}
	public void setGradWork(int gradWork) {
		this.gradWork = gradWork;
	}
	
	
	

}
